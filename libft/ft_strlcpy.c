#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t count;

	count = 0;
	if (size == 0)
		return (ft_strlen(src));
	if (dst == NULL || src == NULL)
		return (count);
	while (src[count] != '\0' && count < (size - 1))
	{
		dst[count] = src[count];
		count++;
	}
	dst[count] = '\0';
	return (ft_strlen(src));
}
